import static java.lang.Math.round;

public class Round {
    public static void main(String[] args) {
        double num_1 = 0.4, num_2 = 0.6;
        float f1 = 0.49f, f2 = 0.50f;
        System.out.println(num_1);
        System.out.println(num_2);
        System.out.println(round(num_1));
        System.out.println(round(num_2));
        System.out.println(round(f1));
        System.out.println(round(f2));

    }
}
